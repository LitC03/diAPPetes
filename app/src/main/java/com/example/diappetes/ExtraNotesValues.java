package com.example.diappetes;

import java.util.Date;

public class ExtraNotesValues {
    String Notes;
    Date Time;

    public ExtraNotesValues(){}

    public String getNotes() {
        return Notes;
    }

    public void setNotes(String notes) {
        Notes = notes;
    }

    public Date getTime() {
        return Time;
    }

    public void setTime(Date time) {
        Time = time;
    }
}
